use crate::error::{ErrorData, Result};
use crate::resource::{ResourceDefinition, ResourceOutputsDefinition, ResourceRef, ResourceType};
use alien_error::AlienError;
use bon::Builder;
use serde::{Deserialize, Serialize};
use std::any::Any;
use std::fmt::Debug;

/// Represents an Azure Container Apps Environment for hosting container applications.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize, Builder)]
#[cfg_attr(feature = "openapi", derive(utoipa::ToSchema))]
#[serde(rename_all = "camelCase", deny_unknown_fields)]
#[builder(start_fn = new)]
pub struct AzureContainerAppsEnvironment {
    /// Identifier for the Container Apps Environment. Must contain only alphanumeric characters, hyphens, and underscores ([A-Za-z0-9-_]).
    /// Maximum 64 characters.
    #[builder(start_fn)]
    pub id: String,
}

impl AzureContainerAppsEnvironment {
    /// The resource type identifier for Azure Container Apps Environments
    pub const RESOURCE_TYPE: ResourceType =
        ResourceType::from_static("azure_container_apps_environment");

    /// Returns the environment's unique identifier.
    pub fn id(&self) -> &str {
        &self.id
    }
}

/// Outputs generated by a successfully provisioned Azure Container Apps Environment.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[cfg_attr(feature = "openapi", derive(utoipa::ToSchema))]
#[serde(rename_all = "camelCase")]
pub struct AzureContainerAppsEnvironmentOutputs {
    /// The name of the Container Apps Environment.
    pub environment_name: String,
    /// The resource ID of the Container Apps Environment.
    pub resource_id: String,
    /// The default domain for applications in this environment.
    pub default_domain: String,
    /// The static IP address of the environment (if applicable).
    pub static_ip: Option<String>,
}

#[typetag::serde(name = "azure_container_apps_environment")]
impl ResourceOutputsDefinition for AzureContainerAppsEnvironmentOutputs {
    fn resource_type() -> ResourceType {
        AzureContainerAppsEnvironment::RESOURCE_TYPE.clone()
    }

    fn as_any(&self) -> &dyn Any {
        self
    }

    fn box_clone(&self) -> Box<dyn ResourceOutputsDefinition> {
        Box::new(self.clone())
    }

    fn outputs_eq(&self, other: &dyn ResourceOutputsDefinition) -> bool {
        other
            .as_any()
            .downcast_ref::<AzureContainerAppsEnvironmentOutputs>()
            == Some(self)
    }
}

// Implementation of ResourceDefinition trait for AzureContainerAppsEnvironment
#[typetag::serde(name = "azure_container_apps_environment")]
impl ResourceDefinition for AzureContainerAppsEnvironment {
    fn resource_type() -> ResourceType {
        Self::RESOURCE_TYPE.clone()
    }

    fn get_resource_type(&self) -> ResourceType {
        Self::resource_type()
    }

    fn id(&self) -> &str {
        &self.id
    }

    fn get_dependencies(&self) -> Vec<ResourceRef> {
        Vec::new()
    }

    fn validate_update(&self, _new_config: &dyn ResourceDefinition) -> Result<()> {
        Err(AlienError::new(ErrorData::InvalidResourceUpdate {
            resource_id: self.id.clone(),
            reason: "Azure container app environments cannot be updated once created".to_string(),
        }))
    }

    fn as_any(&self) -> &dyn Any {
        self
    }

    fn as_any_mut(&mut self) -> &mut dyn Any {
        self
    }

    fn box_clone(&self) -> Box<dyn ResourceDefinition> {
        Box::new(self.clone())
    }

    fn resource_eq(&self, other: &dyn ResourceDefinition) -> bool {
        other
            .as_any()
            .downcast_ref::<AzureContainerAppsEnvironment>()
            == Some(self)
    }
}

