use crate::error::{ErrorData, Result};
use crate::resource::{ResourceDefinition, ResourceOutputsDefinition, ResourceRef, ResourceType};
use alien_error::AlienError;
use bon::Builder;
use serde::{Deserialize, Serialize};
use std::any::Any;
use std::fmt::Debug;

/// Represents an Azure Storage Account for blob, file, table, and queue storage.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize, Builder)]
#[cfg_attr(feature = "openapi", derive(utoipa::ToSchema))]
#[serde(rename_all = "camelCase", deny_unknown_fields)]
#[builder(start_fn = new)]
pub struct AzureStorageAccount {
    /// Identifier for the storage account. Must contain only alphanumeric characters, hyphens, and underscores ([A-Za-z0-9-_]).
    /// Maximum 64 characters.
    #[builder(start_fn)]
    pub id: String,
}

impl AzureStorageAccount {
    /// The resource type identifier for Azure Storage Accounts
    pub const RESOURCE_TYPE: ResourceType = ResourceType::from_static("azure_storage_account");

    /// Returns the storage account's unique identifier.
    pub fn id(&self) -> &str {
        &self.id
    }
}

/// Outputs generated by a successfully provisioned Azure Storage Account.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[cfg_attr(feature = "openapi", derive(utoipa::ToSchema))]
#[serde(rename_all = "camelCase")]
pub struct AzureStorageAccountOutputs {
    /// The name of the storage account.
    pub account_name: String,
    /// The resource ID of the storage account.
    pub resource_id: String,
    /// The primary endpoint for blob storage.
    pub primary_blob_endpoint: String,
    /// The primary endpoint for file storage.
    pub primary_file_endpoint: String,
    /// The primary endpoint for queue storage.
    pub primary_queue_endpoint: String,
    /// The primary endpoint for table storage.
    pub primary_table_endpoint: String,
    /// The primary access key for the storage account.
    pub primary_access_key: String,
    /// The connection string for the storage account.
    pub connection_string: String,
}

impl ResourceOutputsDefinition for AzureStorageAccountOutputs {
    fn get_resource_type(&self) -> ResourceType {
        AzureStorageAccount::RESOURCE_TYPE.clone()
    }

    fn as_any(&self) -> &dyn Any {
        self
    }

    fn box_clone(&self) -> Box<dyn ResourceOutputsDefinition> {
        Box::new(self.clone())
    }

    fn outputs_eq(&self, other: &dyn ResourceOutputsDefinition) -> bool {
        other.as_any().downcast_ref::<AzureStorageAccountOutputs>() == Some(self)
    }

    fn to_json_value(&self) -> serde_json::Result<serde_json::Value> {
        serde_json::to_value(self)
    }
}

// Implementation of ResourceDefinition trait for AzureStorageAccount
impl ResourceDefinition for AzureStorageAccount {
    fn get_resource_type(&self) -> ResourceType {
        Self::RESOURCE_TYPE
    }

    fn id(&self) -> &str {
        &self.id
    }

    fn get_dependencies(&self) -> Vec<ResourceRef> {
        Vec::new()
    }

    fn validate_update(&self, _new_config: &dyn ResourceDefinition) -> Result<()> {
        Err(AlienError::new(ErrorData::InvalidResourceUpdate {
            resource_id: self.id.clone(),
            reason: "Azure storage accounts cannot be updated once created".to_string(),
        }))
    }

    fn as_any(&self) -> &dyn Any {
        self
    }

    fn as_any_mut(&mut self) -> &mut dyn Any {
        self
    }

    fn box_clone(&self) -> Box<dyn ResourceDefinition> {
        Box::new(self.clone())
    }

    fn resource_eq(&self, other: &dyn ResourceDefinition) -> bool {
        other.as_any().downcast_ref::<AzureStorageAccount>() == Some(self)
    }

    fn to_json_value(&self) -> serde_json::Result<serde_json::Value> {
        serde_json::to_value(self)
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_azure_storage_account_creation() {
        let storage_account = AzureStorageAccount::new("my-storage".to_string()).build();
        assert_eq!(storage_account.id, "my-storage");
    }
}
